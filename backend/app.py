def create_app():
    base_dir = os.path.dirname(os.path.abspath(__file__))
    app = Flask(
        __name__,
        template_folder=os.path.join(base_dir, 'templates'),
        static_folder=os.path.join(base_dir, 'static'),
    )
    app.config.from_object(Config)

    # Ensure data directory exists
    data_dir = os.path.join(base_dir, 'data')
    os.makedirs(data_dir, exist_ok=True)

    db.init_app(app)

    # Make icons available in all templates
    @app.context_processor
    def inject_icons():
        return dict(icons=NAV_ICONS)

    # Import and register API blueprints
    from api.auth import auth_bp
    from api.cs2 import cs2_bp
    from api.gym import gym_bp
    from api.weight import weight_bp
    from api.mental import mental_bp

    app.register_blueprint(auth_bp)
    app.register_blueprint(cs2_bp)
    app.register_blueprint(gym_bp)
    app.register_blueprint(weight_bp)
    app.register_blueprint(mental_bp)

    # Auth middleware — protect API routes and page routes (except /login and /static)
    @app.before_request
    def check_auth():
        # Always allow static files and the login page/API
        if request.path.startswith('/static/') or request.path == '/login':
            return
        if request.path.startswith('/api/auth/'):
            return
        # API routes
        if request.path.startswith('/api/'):
            if not session.get('authenticated'):
                return jsonify({'error': 'Unauthorized'}), 401
            return
        # Page routes — redirect to login if not authenticated
        if not session.get('authenticated'):
            return redirect(url_for('login'))

    # ── Page routes ──────────────────────────────────
    @app.route('/login')
    def login():
        if session.get('authenticated'):
            return redirect(url_for('dashboard'))
        return render_template('login.html')

    @app.route('/')
    def dashboard():
        return render_template('dashboard.html', active='dashboard')

    @app.route('/cs2')
    def cs2_page():
        return render_template('cs2.html', active='cs2')

    @app.route('/gym')
    def gym_page():
        return render_template('gym.html', active='gym')

    @app.route('/weight')
    def weight_page():
        return render_template('weight.html', active='weight')

    @app.route('/mental')
    def mental_page():
        return render_template('mental.html', active='mental')

    with app.app_context():
        from models import (CS2Match, CS2Comment, CS2Training, Exercise, GymWeightSet,
                            GymCardioEntry, BodyWeight, MentalHealth)
        db.create_all()

    return app
